    CONSTRAINT fk_rd_linked FOREIGN KEY (linked_account_id) REFERENCES ACCOUNTS(id),
    CONSTRAINT chk_rd_status CHECK (status IN ('ACTIVE', 'MATURED', 'BROKEN'))
