-- Sequences
CREATE SEQUENCE SEQ_ACCOUNT_NO START WITH 1000000000 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_TXN_REF START WITH 100000000000 INCREMENT BY 1 NOCACHE;

-- 1. SYSTEM_CONFIG
CREATE TABLE SYSTEM_CONFIG (
    config_key VARCHAR2(100) PRIMARY KEY,
    config_value VARCHAR2(1000) NOT NULL,
    description VARCHAR2(500),
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

-- 2. ROLES
CREATE TABLE ROLES (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(50) UNIQUE NOT NULL,
    description VARCHAR2(250)
);

-- 3. PERMISSIONS
CREATE TABLE PERMISSIONS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) UNIQUE NOT NULL,
    description VARCHAR2(250)
);

-- 4. USERS
CREATE TABLE USERS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(50) UNIQUE NOT NULL,
    email VARCHAR2(100) UNIQUE NOT NULL,
    password_hash VARCHAR2(255) NOT NULL,
    phone_number VARCHAR2(20) UNIQUE NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    failed_login_attempts NUMBER DEFAULT 0 NOT NULL,
    locked_until TIMESTAMP,
    mfa_enabled NUMBER(1) DEFAULT 0 NOT NULL,
    mfa_secret VARCHAR2(100),
    password_reset_token VARCHAR2(100),
    password_reset_expiry TIMESTAMP,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT chk_user_status CHECK (status IN ('PENDING', 'ACTIVE', 'LOCKED', 'SUSPENDED')),
    CONSTRAINT chk_mfa_enabled CHECK (mfa_enabled IN (0, 1))
);

-- 5. USER_ROLES
CREATE TABLE USER_ROLES (
    user_id NUMBER NOT NULL,
    role_id NUMBER NOT NULL,
    PRIMARY KEY (user_id, role_id),
    CONSTRAINT fk_ur_user FOREIGN KEY (user_id) REFERENCES USERS(id) ON DELETE CASCADE,
    CONSTRAINT fk_ur_role FOREIGN KEY (role_id) REFERENCES ROLES(id) ON DELETE CASCADE
);

-- 6. CUSTOMERS
CREATE TABLE CUSTOMERS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id NUMBER UNIQUE NOT NULL,
    first_name VARCHAR2(50) NOT NULL,
    last_name VARCHAR2(50) NOT NULL,
    date_of_birth DATE NOT NULL,
    pan_number VARCHAR2(10) UNIQUE NOT NULL,
    aadhaar_number VARCHAR2(12) UNIQUE NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    address VARCHAR2(500) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT chk_cust_status CHECK (status IN ('PENDING', 'APPROVED', 'REJECTED', 'SUSPENDED'))
);

-- 7. KYC_DOCUMENTS
CREATE TABLE KYC_DOCUMENTS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    document_type VARCHAR2(50) NOT NULL,
    document_url VARCHAR2(500) NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    uploaded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_kyc_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(id) ON DELETE CASCADE,
    CONSTRAINT chk_kyc_status CHECK (status IN ('PENDING', 'VERIFIED', 'REJECTED'))
);

-- 8. ACCOUNTS
CREATE TABLE ACCOUNTS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    account_number VARCHAR2(20) UNIQUE NOT NULL,
    account_type VARCHAR2(20) NOT NULL,
    status VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    currency VARCHAR2(3) DEFAULT 'INR' NOT NULL,
    open_date DATE DEFAULT SYSDATE NOT NULL,
    close_date DATE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_acc_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(id) ON DELETE CASCADE,
    CONSTRAINT chk_acc_type CHECK (account_type IN ('SAVINGS', 'CURRENT', 'SALARY', 'JOINT', 'FD', 'RD')),
    CONSTRAINT chk_acc_status CHECK (status IN ('ACTIVE', 'FROZEN', 'CLOSED'))
);

-- 9. ACCOUNT_BALANCES
CREATE TABLE ACCOUNT_BALANCES (
    account_id NUMBER PRIMARY KEY,
    ledger_balance NUMBER(18,4) DEFAULT 0.0000 NOT NULL,
    available_balance NUMBER(18,4) DEFAULT 0.0000 NOT NULL,
    version NUMBER DEFAULT 0 NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_bal_account FOREIGN KEY (account_id) REFERENCES ACCOUNTS(id) ON DELETE CASCADE,
    CONSTRAINT chk_bal_positive CHECK (ledger_balance >= 0 AND available_balance >= 0)
);

-- 10. TRANSACTIONS
CREATE TABLE TRANSACTIONS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_reference VARCHAR2(50) UNIQUE NOT NULL,
    source_account_id NUMBER,
    destination_account_id NUMBER,
    amount NUMBER(18,4) NOT NULL,
    transaction_type VARCHAR2(20) NOT NULL,
    channel VARCHAR2(20) NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    description VARCHAR2(250),
    transaction_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_tx_source FOREIGN KEY (source_account_id) REFERENCES ACCOUNTS(id),
    CONSTRAINT fk_tx_dest FOREIGN KEY (destination_account_id) REFERENCES ACCOUNTS(id),
    CONSTRAINT chk_tx_type CHECK (transaction_type IN ('DEPOSIT', 'WITHDRAWAL', 'TRANSFER')),
    CONSTRAINT chk_tx_channel CHECK (channel IN ('NEFT', 'RTGS', 'IMPS', 'UPI', 'ATM', 'WEB')),
    CONSTRAINT chk_tx_status CHECK (status IN ('PENDING', 'PROCESSING', 'SUCCESS', 'FAILED', 'REVERSED')),
    CONSTRAINT chk_tx_amount CHECK (amount > 0)
);

-- 11. TRANSACTION_HISTORY
CREATE TABLE TRANSACTION_HISTORY (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_id NUMBER NOT NULL,
    account_id NUMBER NOT NULL,
    balance_after_txn NUMBER(18,4) NOT NULL,
    entry_type VARCHAR2(10) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_hist_tx FOREIGN KEY (transaction_id) REFERENCES TRANSACTIONS(id),
    CONSTRAINT fk_hist_acc FOREIGN KEY (account_id) REFERENCES ACCOUNTS(id),
    CONSTRAINT chk_hist_entry CHECK (entry_type IN ('DEBIT', 'CREDIT'))
);

-- 12. BENEFICIARIES
CREATE TABLE BENEFICIARIES (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    beneficiary_name VARCHAR2(100) NOT NULL,
    beneficiary_account_no VARCHAR2(20) NOT NULL,
    bank_name VARCHAR2(100) NOT NULL,
    ifsc_code VARCHAR2(11) NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_ben_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(id) ON DELETE CASCADE,
    CONSTRAINT chk_ben_status CHECK (status IN ('PENDING', 'ACTIVE', 'INACTIVE'))
);

-- 13. PAYMENTS
CREATE TABLE PAYMENTS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_id NUMBER NOT NULL,
    provider VARCHAR2(20) NOT NULL,
    external_payment_id VARCHAR2(100) UNIQUE NOT NULL,
    payment_status VARCHAR2(20) NOT NULL,
    refund_status VARCHAR2(20),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_pay_tx FOREIGN KEY (transaction_id) REFERENCES TRANSACTIONS(id),
    CONSTRAINT chk_pay_provider CHECK (provider IN ('STRIPE', 'RAZORPAY'))
);

-- 14. UPI_ACCOUNTS
CREATE TABLE UPI_ACCOUNTS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    account_id NUMBER NOT NULL,
    vpa_address VARCHAR2(50) UNIQUE NOT NULL,
    status VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_upi_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(id) ON DELETE CASCADE,
    CONSTRAINT fk_upi_account FOREIGN KEY (account_id) REFERENCES ACCOUNTS(id) ON DELETE CASCADE,
    CONSTRAINT chk_upi_status CHECK (status IN ('ACTIVE', 'INACTIVE'))
);

-- 15. DEBIT_CARDS
CREATE TABLE DEBIT_CARDS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_id NUMBER NOT NULL,
    card_number VARCHAR2(16) UNIQUE NOT NULL,
    card_holder_name VARCHAR2(100) NOT NULL,
    expiry_date DATE NOT NULL,
    cvv_hash VARCHAR2(255) NOT NULL,
    pin_hash VARCHAR2(255) NOT NULL,
    status VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    daily_limit NUMBER(18,4) DEFAULT 50000.0000 NOT NULL,
    daily_spent NUMBER(18,4) DEFAULT 0.0000 NOT NULL,
    is_virtual NUMBER(1) DEFAULT 0 NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_dc_account FOREIGN KEY (account_id) REFERENCES ACCOUNTS(id) ON DELETE CASCADE,
    CONSTRAINT chk_dc_status CHECK (status IN ('ACTIVE', 'BLOCKED', 'FROZEN')),
    CONSTRAINT chk_dc_virtual CHECK (is_virtual IN (0, 1))
);

-- 16. CREDIT_CARDS
CREATE TABLE CREDIT_CARDS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    card_number VARCHAR2(16) UNIQUE NOT NULL,
    card_holder_name VARCHAR2(100) NOT NULL,
    expiry_date DATE NOT NULL,
    cvv_hash VARCHAR2(255) NOT NULL,
    pin_hash VARCHAR2(255) NOT NULL,
    credit_limit NUMBER(18,4) NOT NULL,
    available_credit NUMBER(18,4) NOT NULL,
    status VARCHAR2(20) DEFAULT 'ACTIVE' NOT NULL,
    billing_cycle_date NUMBER(2) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_cc_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(id) ON DELETE CASCADE,
    CONSTRAINT chk_cc_status CHECK (status IN ('ACTIVE', 'BLOCKED', 'FROZEN')),
    CONSTRAINT chk_cc_cycle CHECK (billing_cycle_date BETWEEN 1 AND 28)
);

-- 17. LOANS
CREATE TABLE LOANS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    loan_type VARCHAR2(20) NOT NULL,
    loan_amount NUMBER(18,4) NOT NULL,
    interest_rate NUMBER(5,2) NOT NULL,
    duration_months NUMBER NOT NULL,
    outstanding_balance NUMBER(18,4) NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    start_date DATE,
    end_date DATE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_loan_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(id) ON DELETE CASCADE,
    CONSTRAINT chk_loan_type CHECK (loan_type IN ('PERSONAL', 'HOME', 'VEHICLE', 'EDUCATION', 'GOLD')),
    CONSTRAINT chk_loan_status CHECK (status IN ('PENDING', 'APPROVED', 'REJECTED', 'REPAYING', 'CLOSED'))
);

-- 18. EMI_SCHEDULE
CREATE TABLE EMI_SCHEDULE (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    loan_id NUMBER NOT NULL,
    installment_no NUMBER NOT NULL,
    due_date DATE NOT NULL,
    principal_amount NUMBER(18,4) NOT NULL,
    interest_amount NUMBER(18,4) NOT NULL,
    emi_amount NUMBER(18,4) NOT NULL,
    status VARCHAR2(20) DEFAULT 'UNPAID' NOT NULL,
    payment_date TIMESTAMP,
    CONSTRAINT fk_emi_loan FOREIGN KEY (loan_id) REFERENCES LOANS(id) ON DELETE CASCADE,
    CONSTRAINT chk_emi_status CHECK (status IN ('UNPAID', 'PAID', 'OVERDUE'))
);

-- 19. FIXED_DEPOSITS
CREATE TABLE FIXED_DEPOSITS (
