-- 28. OUTBOX (Transactional Outbox for Event-Driven Microservices)
CREATE TABLE OUTBOX (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    aggregate_type VARCHAR2(100) NOT NULL,
    aggregate_id VARCHAR2(100) NOT NULL,
    event_type VARCHAR2(100) NOT NULL,
    payload CLOB NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT chk_outbox_status CHECK (status IN ('PENDING', 'PROCESSED', 'FAILED'))
);

